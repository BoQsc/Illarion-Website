<h2>Followers</h2>
<p>Her believers are very few, most are old female druids or elves who have met Ushara personally. She is very well respected among dwarven kind, but true admiration is rarely found. As with all the Elder Gods, she is worshipped at the Temple of the Five, east of Galmair. There is also the Chapel of the Five, east of the Kantabi Desert, where the name of the Five is held in honour. The people of Runewick are known to show more respect for the Five than the citizens of Cadomyr or Galmair - but any miner who finds a gem in the Malachite Mine might thank Ushara in a quick prayer for her creation.</p>
   
<h2>Ushos - Month of Sowing</h2>

<p>Ushos, named after the creator goddess Ushara, is the last month of spring. In Galmair they celebrate the "Crocodile Feast" from the 3rd to 17th Ushos. 
This is a huge, long-lasting street festival in which the people wear animal masks and consume large amounts of alcoholic drinks. Frequently guards have to rescue drunk or even drowned people from the swamps and brooks around Galmair. In Gynk, birthplace of Don Valerio Guilianni, there is even a rumour that the Crocodile Feast was so named because it is a feast for the crocodiles who lurk the stagnant waterways.</p>

