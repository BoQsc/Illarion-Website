<h2>Followers</h2>
<p>Eldan has never been very active amongst mortals. Very few people pray to him because he has rarely been seen in public and only a few know how to identify this god. His worshippers are secretive and there are strange rumours of him wandering around Illarion in disguise. People who follow him are often hermits, or those that love their wine with long philosophical discussions. These followers claim to have met Eldan personally, and instead of praying, they talk or discuss at forlorn places - or in Runewick. He is also worshipped at the Temple of the Five east of Galmair and the Chapel of the Five east of the Kantabi Desert.</p>

<h2>Eldas - Month of Abrosia</h2>

<p>Eldas was named after the creator god Eldan. This month is in all cultures a month of peace and comtemplation. People reminisce about things that life gave them and remember the less fortunate in their prayers. The more devout may even abstain from their beloved or some favorite item they possess in order to remember the worth of all during this time of Eldas. </p>

<p>The Halflings of Yewdale celebrate the last week of Eldas with the so-called "Burrow Days", remembering the resistance at the time of the fall of Firnismill. Traditionally they withdraw into the woods for the entire week and spend the nights in self-made burrows or hollows, only eating fruits of the forest as no fire is allowed. At the end of the week they return in an armed procession to their village creating a great clamour, thus symbolising the recapture of the areas seized by the orcs. More modern halflings that live in towns, along with the upper class of the race, modify this practice by erecting tents in their gardens or maybe an inner courtyard of their house. Sometimes even a meat dish may be served, yet care is made to elaborately decorate it with fruits and mushrooms.</p>

