	<p>Some orc-tribes and mercenary gangs celebrate the blood days (Radosh.) While the
	mercenaries usually use them for wild drinking orgies and scuffles, the Mas days
	have very contrary meanings amongst the orc tribes:</p>

	<p>Some tribes welcome the month with joy and obeisance. They give into the ecstasy
	of fighting and celebrate the evenings wild and boisterous. Moshran, god of blood
	and bones, is the god of war and destruction, bloody revenge and rage. Huge caves
	are lit with torches and fires, and ceremonial fights are held there. It is a common
	belief that some disputes and quarrels are solved in these fights. The most of these
	fights are more of a ceremonial meaning and do not end bloodily.</p>

	<p>Just in a few of the more wild and uncivilized tribes the warriors "slay each
	another in heaps". It is said that the wildest tribes sacrifice people - usually not
	captives from other tribes and races, but members of their own tribe who give
	themselves to the sacrifical dagger. It is rumoured that these tribes believe that
	this death would be the biggest honor that a warrior could have, and that someone
	who died that way would be reborn under Moshran's blessing in a strong body. This
	custom, als well as the ritus of "slay each another in heaps" has becomes very rare,
	because the loss of any good warrior or hunter usually is a hard blow for any
	tribe.</p>

	<p>Generaly, animals are slaughtered and sacrificed to the gods and to the
	ancestors. The meat of these animals is eaten in the evenings. For many orcs, these
	days are also a memorial to one of their greatest leaders in history: Drokan
	Norotar, who is honored highly as a hero and martyr. A long time ago, he united the
	peaceful tribes of the orcs and gave the orcish people a voice amongst all other
	races. But the neighbours of his state did not trust his peaceful intentions. Under
	the cover of negotiations he was trapped and murdered. The young state broke apart
	and what followed brought much suffering over the orcs. The flame-orcs see Drokan
	Norotar as a prophet who brought peace and civilisation under the wild tribes.
	Although he himself was no flame orc, some of his writings had great influence on
	the culture of their town fortresses. There is even a prophecy saying that a second
	Drokan Norotar or even his reborn soul will soon again reunite the tribes.</p>

	<p>In his memorial, the "wild" tribes of the orcs smear ashes into their face and
	cut themselves with daggers and sword blades into their hands and scalps in
	rememberance that Drokan Norotar was beheaded and his hands had been cut off: As a
	sign that the orcs are leader- and powerless. With their blood-covered faces they
	chant dirges and elegies, which are kept going over all six days.</p>

	<p>In other races this feast has the more symbolic tradition of sacrificing a small
	animal, usually a chicken or rabbit, without many words. This is done to calm
	Moshran and to prevent wars from happening, and to keep away bad luck. Usually there
	is no prayer, just a silent gesture. After the sacrifice, the people stay in their
	homes and hope that the Mas days are past soon, so a new year can come.</p>
