	<h1>Siros</h1>

	<h2>Month of Love</h2>

	<p>The month "Siros" is named after Sirani, the goddess of love who is popular among
	the people of Salkamar, Gynk and the Serinjahs.</p>

	<p>All known cultures have loyalty and binding celebrations during the month of
	Siros. Those mostly jolly fairs offer young couples the possibility for marriage
	vows. There is no precise date for that festival but it is usually held on the 12th
	Siros. Often villages communicate with each other so that there can be a festival in
	each village on a different day.</p>

	<p>Although all peoples celebrate there are differences among the races and
	religions.</p>

	<p>The elven tradition expects that the man attaches branches of the Mevranno-tree
	to the entrance of his love's house. This tree lives in symbiosis with another plant
	called Levrenni. Levrenni is a blooming climbing plant who starts to wrap around
	trees already when it is still young. If one of the two plants tries to exist alone,
	it cannot survive for a long duration. If the woman agrees to the man's offer she
	braids a Levrenni plant around the Mevrannon branch that the elf brought.</p>

	<p>Among the halflings it is the women who do the first step. On the day of the
	festival they put a girdle of flowers on top of their love's head. If the man does
	not take it off, the offer is accepted.</p>

	<p>The human tradition does not follow a straight line and is rather unclear. That
	might be because of the humans' open-minded method of handeling this topic. There is
	no real offer but the expression of love. Still, also among the humans there are
	great differences between the locals. Things that might count as honourable and good
	in the one village could be seen as highly insulting behaviour in the other. Most
	well-known is the strategy of firstly asking the love for a dance. If the woman
	wishes more than just a nice dance she gives the man a piece of colourful fabric
	which is braided into her hair. This fabric is the traditional sign of sympathy. If
	the man binds the fabric around his arm, he signals that he returns the feeling.</p>

	<p>Orcs are known for their rough method of expressing sympathy. Following their
	tradition the man secretly visits the house of the woman with his friends and
	"steals" her. This thievery is sort of a ritual so that there are usually no
	injuries received on each side. Still there even were some deads, especially when
	the woman already sent signs of rejection earlier which were ignored. In general the
	woman is immediately brought to the man's house. The family of the woman tries to
	"save" her. Therefore they all gather in front of the house. If the "kidnapped"
	woman opens the door and asks the family to come inside before the house is stormed
	the marriage offer is taken accepted.</p>

	<p>Lizards are special because most weddings are arranged by the parents and the
	children are not allowed to speak up. Therefore it is not impossible that the couple
	just meets the first time in their life on the festival. Before the celebration the
	parents already decide certain agreements. You then meet in the house of the husband
	and all together they go to the temple. There the marriage vow is spoken in front of
	a priest.</p>

	<p>In case of the dwarves the local smith has an important role in the act of
	preparing and finally excercising the wedding. Being a smith does not only mean to
	do a craft but also a smith's doings are most spiritual and religious. Smiths are
	able to craft linkages of the most different materials, they can even make magical
	weapons. A couple that wants to marry is therefore "linked" by the smith via two
	iron rings that are attached to each other. Each ring has an own identity and even
	forms an own unit but still both are tightly embracing. By that you express the
	character and the duration of the linkage and that is why you pin the rings above
	the couple's bed.</p>
